                <h2> <i>  “Op welke wijze kan smartphone gebruik worden ingezet om het fietsverkeer veiliger te maken?” </i> </h2>
                    <p> Tegenwoordig maakt smartphone gebruik een groot en belangrijk deel uit van ons leven. In Nederland weet men dat het gebruik van smartphones in het verkeer levensgevaarlijk kan zijn, toch worden de gevaren op grote schaal genegeerd. Het is zelfs zo ernstig dat één op de vijf ongelukken onder jongeren te maken heeft met smartphone gebruik. Van de jongeren in Nederland (12-18 jaar) heeft bijna iedereen een smartphone bundel en 77% hiervan heeft aangegeven deze regelmatig op de fiets te gebruiken (Sebbar, 2017). Er zijn op dit moment meerdere partijen bezig met het vinden van een oplossing, de overheid en een groot deel van de Nederlanders wil echter dat er een verbod op appen op de fiets komt, ingaande vanaf 2019 (SAMR, 2017). Met dit project zal er worden gekeken naar welke smartphone functies een gevaar vormen in het fietsverkeer en of er alternatieve oplossingen zijn of dat een verbod daadwerkelijk de beste oplossing is voor dit probleem. </p>
                </div>

                <div id="content2">
                    <div class="client-content">
                        <p> De opdracht komt vanuit het lectoraat User-Centered Design van de Hanzehogeschool. Het lectoraat probeert nieuwe kennis op te doen over het ontwerp van digitale gereedschappen om zo binnen een bepaalde doelgroep voor een verandering te zorgen in kennis en/of gedrag. Hierbij ligt de focus op adaptieve systemen, dat wil zeggen dat de gereedschappen (tools) zich kunnen aanpassen aan de individuen, nieuwe technologie en de maatschappij. Deze systemen worden zo snel mogelijk toegepast in samenwerking met bedrijven uit de creatieve industrie en andere partners (HanzeHogeschool, 2018). 
                        Daarnaast zijn er nog een aantal stakeholders/secundaire opdrachtgevers, dit zijn New Nexus Mobile en Regionaalorgaan Verkeersveiligheid Friesland. New Nexus Mobile is een bedrijf dat zich al 15 jaar bezig houdt met ICT, voornamelijk applicaties, hierbij probeert ze het gebruikelijke te combineren met nieuwe technologieën.  Regionaalorgaan Verkeersveiligheid Friesland (ROF) houdt zich bezig met het bevorderen van de verkeersveiligheid in Friesland. Dit doen ze doormiddel van campagnes, activiteiten op het gebied van verkeerseducatie en projecten. Daarnaast is het ROF koploper op het gebied van actief en innovatief verbeteren van de verkeersveiligheid (ROF, 2018). </p>
                    </div>
                    <div class="client-image">
                        <img src="images/opdrachtgever.jpeg" alt="image" width="" height="">
                    </div>
