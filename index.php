                    <div class="tech-content"> 
                        <p> Dit probleem speelt al een langere periode, er zijn daarom ook al een aantal oplossingen bedacht. Eén van die oplossingen is het slimme fietsslot van KPN bedoeld voor kinderen. Het fiets slot staat in verbinding met de smartphone, zodra het fiets slot open is wordt de netwerk verbinding van de smartphone verbroken (Nu.nl, 2017). Een mooi voorbeeld van smartphone integratie om het fiets verkeer veiliger te maken is de Smart Jacket van Vodafone. De smart jacket is puur voor het veilig navigeren door het verkeer in het donker. Zodra de smartphone contact maakt met de Smart Jacket wordt de route geprogrammeerd en wanneer de bestuurder dan naar rechts of links moet, wordt dit weergegeven door middel van rode pijlen op de rug van de fietser. De fietser zelf kan op zijn mouw zien welke kant hij of zij op moet (technology, 2016). Een laatste oplossing voor dit probleem is de smart bicycle, Volata 1C. Dit is een fiets met ingebouwd schermpje en knoppen op het stuur, deze kan door middel van bluetooth verbinden met de smartphone (Ringelestijn, 2017). Deze oplossing is op dit moment nog vrij prijzig maar heeft zeker potentie.  </p>
                    </div>
                </div>

                <div id="content5">
                    <p> Het is urgent en zeker belangrijk voor de maatschappij dat er snel een oplossing komt voor dit probleem. Dit komt met name door het dreigende verbod wat waarschijnlijk voor nieuwe problemen gaat zorgen. Maar ook door de snelle ontwikkelingen binnen de technologie, wie weet wat er straks allemaal mogelijk is met een smartphone. Het verbod zou op den duur de technologie in de weg kunnen gaan staan. Daarom is het belangrijk dat er een goede oplossing komt die een middenweg vormt tussen veiligheid en gemak. </p>
                </div>


                <div id="content6">
                    <p> Mijn kwaliteiten liggen vanwege mijn huidige studie richting (interactive Media Technologies) voornamelijk in het ontwikkelen van websites. Een groot onderdeel hiervan is het constant bedenken van oplossingen voor problemen waar je tegen aan loopt (trial and error). 
                    Daarnaast heb ik twee jaar Human Technology gestudeerd (tegenwoordig Industrieel product ontwerp) aan de Hanzehogeschool. Bij deze studie ligt de focus op het doen van onderzoek en een oplossing vinden voor allerlei problemen. Het gaat hierbij zowel om fysieke als digitale producten en zelfs ruimtes. Zo heb ik zelfs een keer een slijterij in Paddenpoel verbeterd. Andere opdrachten die ik met deze studie heb uitgevoerd zijn onder andere voor de ov chipkaart, straatverlichting en een medische applicatie. 
                    Met mijn huidige studie ben ik vooral bezig geweest met digitale producten voor bijvoorbeeld de bloedbank van Nederland (Sanquin) en Dienst ICT en Uitvoering (DICTU). Producten die ik hierbij gemaakt heb zijn voornamelijk websites, applicaties en dashboards. Door de kennis die ik heb opgedaan bij deze twee studies en het werken met opdrachtgevers denk ik dat ik mooi kan bijdragen aan een oplossing voor dit probleem.  </p>

                    <p> Ik zou van dit project graag willen leren wat de invloed van smartphone gebruik is. Zelf zit ik constant op mijn telefoon, maakt niet uit waar ik me bevind, ook al krijg ik bijvoorbeeld geen berichten dan nog is het een soort automatisme dat ik er even op moet kijken. Ik hoop dat ik mijn eigen smartphone gebruik en daarbij mijn veiligheid kan verbeteren door middel van dit project. </p>
