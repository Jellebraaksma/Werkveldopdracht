                <div id="content3">
                    <p> De thema’s die bij dit project horen zijn multitasking, road safety (verkeersveiligheid) en distraction (afleiding). Het domein waar dit project binnen valt is stad van de toekomst. Wat opvalt is dat als we kijken naar het verkeer in de stad van de toekomst, dan ligt de focus voornamelijk op het autoverkeer. Er wordt ontzettend hard gewerkt aan het maken van zelfrijdende auto’s. Het AD stelt dat een autobezitter gemiddeld ongeveer 37.500 uur in zijn leven zal besparen. Daarnaast zal het aantal verkeersongelukken afnemen en er zullen minder auto’s nodig zijn (Kouwenhoven, 2018) 
                    Als we naar de stad van de toekomst in het algemeen kijken is duurzaamheid ook erg belangrijk. De natuur staat centraal en voertuigen moeten zo min mogelijk belastend hiervoor zijn (Jelyia, 2017) Dit is een extra reden om een slimme oplossing te vinden voor de probleemstelling om fietsgebruik te stimuleren waarbij technologie niet in de weg komt te staan. </p>
                </div>

                <div id="content4">
                    <div class="tech-image">
                      <img src="images/tech.jpeg" alt="image" width="" height="">
